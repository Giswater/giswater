package org.giswater.model;


public class RptToken {

	private String rptName;
	private String rptValue;
	
	
	public RptToken(String rptName, String rptValue) { 
		this.rptName = rptName;
		this.rptValue = rptValue;
	}
	

	public String getRptName() {
		return rptName;
	}

	public void setRptName(String rptName) {
		this.rptName = rptName;
	}

	public String getRptValue() {
		return rptValue;
	}

	public void setRptValue(String rptValue) {
		this.rptValue = rptValue;
	}

	
}